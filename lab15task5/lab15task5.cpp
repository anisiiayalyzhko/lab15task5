#include <stdio.h>

int main() {
	//масив на 10 елементів
	int array[10], i;
	//виводимо завдання для користувача
	printf("Enter 10 numbers:\n");
	//ввід десяти єлементів масиву
	for (i = 0; i < 10; i++) {
		scanf_s("%d", &array[i]);
	}
	printf("\n\n");
	//виводимо масив в зворотному порядку
	for (i = 9; i >= 0; i--) {
		printf("%d\n", array[i]);
	}
	return 0;
}

